//////////////////  LOGOUT API ////////////////////////////////////

router.post('/logout', logout);
///////////////////////////////////////////////////////////////////

//////////////////  find user for forget password API ////////////////////////////////////

router.post('/forget-password/find-account', forgetPassword);
///////////////////////////////////////////////////////////////////

///////////////////////////*******************////////////////////////////////////////

export default router;
